// ============================================================
// ヘッダーのスタイル定義
// ============================================================
const headerFill = {
  type: "pattern",
  pattern: "solid",
  fgColor: { argb: "FF1976D2" },
};

const headerFont = {
  bold: true,
  color: { argb: "FFFFFFFF" },
  size: 11,
};

const headerAlignment = {
  horizontal: "center",
  vertical: "middle",
  wrapText: true,
};

const headerBorder = {
  top: { style: "thin" },
  left: { style: "thin" },
  bottom: { style: "thin" },
  right: { style: "thin" },
};

const cellBorder = {
  top: { style: "thin", color: { argb: "FFD0D0D0" } },
  left: { style: "thin", color: { argb: "FFD0D0D0" } },
  bottom: { style: "thin", color: { argb: "FFD0D0D0" } },
  right: { style: "thin", color: { argb: "FFD0D0D0" } },
};

const categoryFill = {
  type: "pattern",
  pattern: "solid",
  fgColor: { argb: "FFF5F5F5" },
};

// ============================================================
// シートを構築する関数
// ============================================================
function buildSheet(worksheet, features, sheetTitle) {
  // カラム定義
  worksheet.columns = [
    { header: "機能カテゴリ", key: "category", width: 30 },
    { header: "機能詳細", key: "detail", width: 55 },
    { header: "MVP", key: "mvp", width: 8 },
    { header: "成長期", key: "growth", width: 10 },
    { header: "確認事項", key: "notes", width: 30 },
  ];

  // ヘッダー行のスタイル
  const headerRow = worksheet.getRow(1);
  headerRow.eachCell((cell) => {
    cell.fill = headerFill;
    cell.font = headerFont;
    cell.alignment = headerAlignment;
    cell.border = headerBorder;
  });
  headerRow.height = 25;

  // データ行を追加
  features.forEach((row) => {
    const dataRow = worksheet.addRow({
      category: row[0],
      detail: row[1],
      mvp: "✅",
      growth: "",
      notes: "",
    });

    dataRow.eachCell((cell, colNumber) => {
      cell.border = cellBorder;
      cell.alignment = { vertical: "middle", wrapText: true };

      // MVP列は中央揃え
      if (colNumber === 3) {
        cell.alignment = { horizontal: "center", vertical: "middle" };
      }
      // 成長期列も中央揃え
      if (colNumber === 4) {
        cell.alignment = { horizontal: "center", vertical: "middle" };
      }

      // カテゴリセルに背景色
      if (colNumber === 1 && row[0] !== "") {
        cell.fill = categoryFill;
        cell.font = { bold: true, size: 11 };
      }
    });
  });

  // フィルターを設定
  worksheet.autoFilter = {
    from: "A1",
    to: `E${features.length + 1}`,
  };

  // 印刷設定
  worksheet.pageSetup = {
    orientation: "landscape",
    fitToPage: true,
    fitToWidth: 1,
    fitToHeight: 0,
  };
}

// ============================================================
// メイン処理
// ============================================================
async function main() {
  const workbook = new ExcelJS.Workbook();
  workbook.creator = "Community Platform";
  workbook.created = new Date();

  // 利用者シート
  const userSheet = workbook.addWorksheet("利用者");
  buildSheet(userSheet, userFeatures, "利用者");

  // 管理者シート
  const adminSheet = workbook.addWorksheet("管理者");
  buildSheet(adminSheet, adminFeatures, "管理者");

  // システム管理者シート
  const sysAdminSheet = workbook.addWorksheet("システム管理者");
  buildSheet(sysAdminSheet, systemAdminFeatures, "システム管理者");

  // 出力
  const outputPath = path.join(__dirname, "..", "docs", "機能一覧.xlsx");
  await workbook.xlsx.writeFile(outputPath);
  console.log(`Generated: ${outputPath}`);
  console.log(`  利用者シート: ${userFeatures.length} 機能`);
  console.log(`  管理者シート: ${adminFeatures.length} 機能`);
  console.log(`  システム管理者シート: ${systemAdminFeatures.length} 機能`);
}

main().catch((err) => {
  console.error("Error:", err);
  process.exit(1);
});
